        <h2>By-laws</h2>
        
		<p>I. PURPOSES<br />
          This Chapter shall implement the purposes of USITT as stated in Article II, Section I of the by-laws of USITT and shall encourage the performing arts as a right rather than a privilege for all the people of North Dakota and especially the Eastern North Dakota area; therefore we will encourage and stimulate the finding of performance artists, and the creation of production facilities and audiences in the state of North Dakota and particularly in our immediate area. </p>
        
		<p>II. MEMBERS<br />
          Membership in this Chapter shall not be limited to members in good standing of USITT; neither shall it be limited to students at North Dakota State University, but to college and university students in the immediate area that for reasons unto themselves cannot form Chapters at their own colleges and universities.</p>
        
		<p>III. MEMBERSHIP MEETINGS<br />
          This chapter shall follow Article II, Section 3 of the USITT (national) by-laws except that special meetings may be called by the Executive Council (rather than the Board of Directors). Two-thirds shall constitute a quorum (rather than five percent as noted in the national by-laws).</p>
        
		<p>The term &quot;Chapter&quot; shall be substituted for the term &quot;Corporation&quot; as used in the national by-laws.</p>
        
		<p>IV. OFFICERS<br />
          General Officers.   The officers of this Chapter shall be:</p>
		
		<p>a. Chair<br />
			b. Vice Chair<br />
            c.  Secretary<br />
            d. Treasurer<br />
            e.  Events Coordinator/Recruiter</p>
		
		<p>The Officers shall be elected by the membership of the Chapter at the last regular meeting of the Spring semester. Any officer may be removed at any time by an affirmative vote of two-thirds of the active membership of the Chapter. </p>
		
		<p>A. CHAIR<br />
  The Chair shall preside at meetings and shall take general charge of the activities of the Chapter. In addition, s/he shall be responsible
  for the periodic Chapter reports to the USITT Vice President for Sections &amp; Chapters. The annual report to the VIce President for
  Sections &amp; Chapters, which is required by USITT for annual action by the Board of Directors in order to renew the Chapter Charter,
  must be prepared by the Chair for forwarding to the Vice President for Sections &amp; Chapters to arrive on or before the appointed date
  each year. The Chair shall be responsible for the appointment of the various Chapter committees.</p>
		
		<p>B. VICE CHAIR<br />
  The Vice Chair shall, in the absence of incapacity of the Chair, perform all the functions of the Chair and shall such duties as may be assigned to him/her by the Chair.</p>
		
		<p>C. SECRETARY<br />
  The Secretary shall act as secretary for all meetings of the members, the Executive Committee and the officers, and keep minutes thereof.
  The Secretary shall keep and file all records of meetings. The Secretary shall notify all members of the Chapter of special and annual meetings
  and record the proceedings therof. The Secretary shall perform other duties as may be assigned by the Chair.</p>
		
		<p>D. TREASURER<br />
  The Treasurer shall be responsible for the care and custody of the funds of the Chapter and, subject to the review of the Executive Committee, shall be responsible for depositing same in the name of the Chapter in such banks, trust companies,
  and safe vaults as the Executive Committee may designate. The Treasurer shall supervise the collection of and receipt of charges and moneys
  payable to the Chapter and shall have supervision of the books and records showing all receipts and expenditures. An audit of these
  books and records must be provided to the Executive Committee at the end of each school year. The Treasurer shall perform other duties
  as may be assigned by the Chair.</p>
		
		<p>E. EVENTS COORDINATOR/RECRUITER<br />
  The Events Coordinator/Recruiter shall be responsible for any possible new memberships to the Chapter. The Events Coorinator/Recruiter shall supervise the publicity of the Chapter amongst the community, North Dakota State University, and any fellow
  universities and colleges in the immediate area.</p>
		
		<p>V. EXECUTIVE COMMITTEE</p>
		
		<p align="left" class="bodytext"> A.  The Committee shall consist of the Chapter Chair, Vice Chair, Secretary, Treasurer and the Chair of any committees, plus one Member at Large elected by the membership. The committee shall approve the agenda of any regular or special meeting of the Chapter.</p>
		<p>B.  Meetings of the Executive Committee shall be held at such times and places as may be fixed by a majority of the Executive Committee.</p>
		<p>C. The Executive Committee shall be empowered to carry on all regular business of the Chapter. In all such meetings it shall be guided in good
  faith by policies previously established by the Chapter and USITT. </p>
		
		<p>D. A quorum shall consist of three officers, one of whom must be the Chair or the Vice Chair, and one Member at Large, and one committee Chair,
  if there be any.</p>
		
		<p>VI. FACULTY SPONSOR</p>
        
          <p> A.  The Chapter must have at least one faculty sponsor who is a member in good standing of USITT. More than one sponsor may be
  invited to be a faculty sponsor.</p>
          <p>B. The term for each sponsor is from the final meeting in May until the final meeting of the following May. </p>
          <p>C. The members of the Chapter may invite a faculty member to be a sponsor only after an affirmative vote in favor of the
  faculty member has been given by a majority of the Chapter members. Annual renewal of the invitation shall be extended to the sponsor only after
  an affirmative vote in favor of the faculty member has been given by a majority of the Chapter members.</p>
          <p>D. The invitation for sponsorship may be withdrawn at any time provided that the Chapter members have voted, and two-thirds
  of those voting have voted against the retention of the faculty member as a sponsor.</p>
        
        <p>VII. All contracts, agreements, checks and other negotiable instruments and endorsements thereof shall be signed by the Treasurer
          and the Faculty Sponsor. </p>
        <p>VIII. FISCAL YEAR<br />
          The fiscal year of the Chapter shall commence on the first day of May each year.</p>
        <p>IX. AMENDMENTS</p>
        
          <p> A. Proposal for amendments. These by-laws may be amended at any regular or special meeting of the Chapter in the following manner:<br />
        <br />
  Any proposal, for such amendment, shall be in writing, subscribed to by not fewer than five percent of the members of the Chapter and filed
  with the Secretary of the Chapter. Upon such filing the Secretary shall notify the membership of the Chapter in the next regular announcement.
  The date of the meeting shall not be less than ten days nor more than twenty days following the date the members were notified of such a
  meeting.</p>
          <p>B. Adoption of Amendments. An amendment shall be adopted by an affirmative vote of at least two-thirds of the membership
  present at the meeting called to consider the adoption.</p>
          <p>C. Notice: The Secretary shall mail to otherwise distribute a copy of each amendment to all members of the chapter,
  and to the USITT VIce President for Sections &amp; Chapters within thirty days of the adoption of the amendment.</p>
       
        <p>Forwarding a copy of the by-laws and amendments thereto, the Secretary and Treasurer of the Northern Boundary Section of USITT
          shall be done as a matter of courtesy. Such items are for information only, as the Chapter is not a subsidiary of the Northern Boundary Section.</p>
