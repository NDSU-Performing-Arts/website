<h2>Further Information about Ensembles  </h2>
  
 <p style="margin-left: 40px; margin-bottom: 0px;"> 
	•	Large ensembles are smaller in the number of players/singers and often have multiple subgroups, rehearsing separately.<br>  
  	•	Choirs will frequently divide into octets; Wind Symphony will sometimes divide into chamber ensembles. Other ensembles may use innovative formats. <br>
  	•	Spacing while performing is at least 6 feet. <br>
  	•	Rehearsals and performances are limited to 40 minutes, followed by a minimum of a 20-minute break.  This practice allows for dispersion of aerosols.	<br>
  	•	Bell covers for instruments are used to reduce aerosol spread.  However, bell covers are not required for saxophones, bass clarinets, and bassoons, as covers have not been shown to reduce aerosols in these instruments.
    <br>
    •	Performers must use paper plates and paper towels (furnished) to collect condensation from instruments during rehearsals <br>
