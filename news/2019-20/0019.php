	<li>Honoring challenging discussions and ideas that oppose structural norms.</li>
    <li>Celebrating diverse artists through classroom curriculum, discussion, and production.</li>
	<li>Examine and replace Division and University policies that disadvantage underrepresented groups.</li>
	</ul>
