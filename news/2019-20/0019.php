      <p>  To our students, alumni, colleagues, audiences, and fellow artists: </p>
        
    
      <p>The recent deaths of George Floyd, Breonna Taylor, Ahmaud Arbery, Tony McDade, Rayshard Brooks, and so many others have once again revealed significant historical inequalities and pervasive racism that exist in our society. We are outraged by these senseless and tragic deaths, and we stand in solidarity with our communities of color.</p>

<p>We write today to affirm the Division of Performing Arts’s commitment to supporting communities of color. We wholeheartedly condemn racism, bigotry, violence, and aggression against those who identify as Black, Indigenous, and People of Color (BIPOC). The Division is committed to celebrating your voices, experiences, visions, expressions, outlooks, and stories as essential to our future together in the classroom, production, and performance as well as in the world beyond NDSU. </p>

<p>Diversity, equity, and inclusion are central pillars of the Division of Performing Arts’s artistic and academic mission, and they encompass the fair treatment of all members of our community. We are committed not only to respecting all races and ethnicities, but we also extend our commitment to students, alumni, colleagues, audiences, and fellow artists of all sexualities and gender expressions as well. </p>

<p>The Division of Performing Arts pledges to develop anti-racist practices and guidelines that will be upheld throughout the Division. In doing so, we will continue our mission as a student-focused, land-grant university.</p>

<p>The Division of Performing Arts commits to:</p>
	  
<ul>
	 <li> Acknowledging the trauma of and listening to our BIPOC students, alumni, colleagues, audiences, and fellow artists.</li>	
