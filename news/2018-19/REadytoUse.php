		<p><b>Fargo, N.D.</b> - The NDSU Wind Symphony, led by Professor Warren Olfert, embarked on a tour of Eastern Europe, visiting and performing in Budai Vigadó (Hungarian Heritage House) in Budapest, the Slovak Radio Hall in Bratislava, and culminating with a prestigious performance in the Spanish Hall in the Prague Castle to a sold-out audience of over 600 dignitaries. The group was the first American ensemble to ever perform in the Spanish Hall. Over 65 students, faculty, chaperones, and alumni participated in this once-in-a-lifetime experience.</p>

		<p>In the Fall of 2017 on campus, the NDSU Wind Symphony had the opportunity to work with Colonel Václav Blahunek, conductor of the Prague Castle Guard and Czech Symphonic Police Band. It was through this experience, and the impression made upon Col. Blahunek, that the NDSU Wind Symphony was invited by The Ministry of Culture for the Czech Republic to perform with the Czech Police Symphonic Police Band in the Spanish Hall in the New Royal Palace of Prague Castle. The Spanish Hall is rarely used and only for special events of the state. After the concert, Colonel Blahunek expressed, “I enjoyed your concert so much!  The program for the concert was something I have never experienced with any American Symphonic Band; you led listeners with a strong musical sense. Your interpretation was fantastic!”.</p>
		
		<p>Director Olfert explained that this was the first overseas trip for the concert band, “exceeded all of my expectations, and really was one of the great experiences for the students and me.  I’m so proud of the students and excited that we were able to see this tour come to pass.”</p>
		
	  <p>NDSU President Dean Bresciani traveled to Prague for this prestigious event and had the opportunity to tour the Prague Castle prior to the performance. During the tour, he and his group inquired about viewing the Spanish Hall and their guide explained to them that it was locked and rarely used other than for events of state. They let their surprised guide know that they’d be attending a concert in the hall the following night, and were looking forward to being in such a renowned space. </p>
		
		<p>Having the opportunity to travel and perform abroad for student clarinetist Lauren Anne Sampson was “such a surreal experience” and “was one of the most rewarding experiences I will ever have throughout my college career”. </p>
		
		<p>Graduate student hornist Matthew Winarski expressed that “performing with the NDSU Wind Symphony in the Prague Castle was something I am not going to forget anytime soon. Playing in front of dignitaries, government officials, and people of the Czech Republic was thrilling. The Prague Castle is a beautiful, ancient building with a rich history. It was truly a great honor to perform in the Spanish Hall—a room reserved for official guests of the Czech Republic”.</p>
		
		<p>The North Dakota State University Wind Symphony is the premier collegiate wind band at NDSU and is known for its excellence and innovative programming throughout the region. Students who participate experience the highest quality of music literature. Comprised of both Music Majors and Non-Majors, musicians of the NDSU Wind Symphony come from a wide array of NDSU academic disciplines. Under the direction of Dr. Warren Olfert, students work tirelessly from practice room to the concert hall to create musical artistry for audiences across the Midwest and the nation.</p>
		
		
		
