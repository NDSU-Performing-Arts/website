	<p>To meet the goals of the symposium, the NDSU Challey School of Music choral faculty, Jo Ann Miller, Michael Weber and Charlette Moe, searched for a choral work that would embody the focus and intent of the symposium.  Considering Matthew Shepard, by Craig Hella Johnson, is exactly the type of choral work that tells a story, educates the audience and inspires dialogue about a major social issue.</p>
			
	<p>In October of 1998, Matthew Shepard, a young, gay student at the University of Wyoming in Laramie was kidnapped, severely beaten, tied to a fence and left to die in a lonely field under a blanket of stars. Five days later, when Matt passed away, the world was watching. Approaching the eve of the 20th anniversary, Craig Hella Johnson has responded with his first concert-length work, Considering Matthew Shepard.</p>
			
	<p>Led from the piano by Johnson, <i>Considering Matthew Shepard</i> showcases the award-winning artistry of Conspirare’s singers with a chamber ensemble of renowned instrumentalists. This three-part fusion oratorio speaks with a fresh and bold voice, incorporating a variety of musical styles seamlessly woven into a unified whole. Johnson sets a wide range of poetic and soulful texts by poets including Hildegard of Bingen, Lesléa Newman, Michael Dennis Browne, and Rumi. Passages from Matt’s personal journal, interviews and writings from his parents Judy and Dennis Shepard, newspaper reports and additional texts by Johnson and Browne are poignantly appointed throughout the work.</p>
		
	<p>Please join us for the concert and stay for the talk-back with the composer, Craig Hella Johnson.  This is a great opportunity for all of us to come to a better understanding of this social issue and a better feeling of acceptance and love within our community.</p>

