        <p>Mozart's 1786 masterwork, <em>The Marriage of Figaro</em>, casts a revolutionary eye on privilege and rank. Based on a banned play by French author Beaumarchais, it describes Count&nbsp;Almaviva's efforts to revive the hated &quot;doit du Seigneur,&quot; or the right of a nobleman to sleep wtih his servantgirls before they marry. Figaro, formerly the well-respected barber of Seville, is now the Count's valet. He is forced to bring all his wits to bear in thwarting the Count's desire for Figaro's fiancee, Susanna, who is the Countess Almaviva's personal lady in waiting. Librettist Lorenzo da Ponte removed much of the revolutionary rhetoric, and Mozart's own musical interests centered on the human aspects of fidelity, infidelity, and forgiveness. Nonetheless, musicologist Catherine Clement has written that Figaro's lively aria, &quot;If the little&nbsp;Count wants to dance, I will play the guitar,&quot; sung through the clenched teeth of rage, prefigures the violence of the French Revolution. Guest stage director Walker Lewis adds a further twist to the plot by updating the action to the Summer of Love, circa 1967.</p>
        
