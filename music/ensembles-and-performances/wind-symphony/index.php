	<p><strong><u>Who Needs to Audition</u></strong></p>
	
	<p>All instrumental music majors and minors will need to audition for placement in ensembles (you will be placed in Symphony Orchestra, Wind Symphony, or University Band). Any non-music major wanting to play in Symphony Orchestra and/or Wind Symphony will also need to audition. Non-music majors do not have to audition if they intend to play in University Band.</p>

	<p><strong><u>Audition Sign-Ups</u></strong></p>

	<p>This Fall, all auditions will take place on Wednesday, August 28th. In the shared Google folder above, open the file labeled “Instrumental Auditions Signup – Fall 2024” and click on the tab with your instrument listed. Put your name and instrument in any of the available time slots that work for you. Please do not remove or alter another student’s signup, unless you’ve worked out a change with them. Instructor names and email addresses are listed on these sheets.</p>

	<p><strong><u>Audition Repertoire</u></strong></p>

	<p>Audition repertoire can also be found in the shared Google folder linked above. Please download your excerpts to a personal device or print on your own. Plan on bringing your own copy (on paper or on your device) into the room for auditions.</p>

	<p><strong><u>Day of Auditions</u></strong></p>

	<p>On the day of auditions, please make sure you are warmed up and ready to play as soon as you enter the room. Although there are signup times, auditions often run a little ahead or behind, so it may be a good idea to check on how the room is doing when you first arrive, before your warm-up. While waiting in line outside the audition room, it’s fine if you talk quietly with others or finger through things, just please don’t play your instrument or speak loudly. Again, make sure you have your own copy of the music ready in case your audition room doesn’t have a copy for you. Feel free to dress casually/comfortably.</p>

	<p><strong><u>Audition Results</u></strong></p>

	<p>Because these auditions all take place on Wednesday, 8/28, our goal/plan is to announce the ensemble placements early on Thursday. However, if we run into the issue of being unable to hear all students in a given studio that Wednesday, the results for that studio may be held until we’ve had a chance to hear all students.</p>

	
