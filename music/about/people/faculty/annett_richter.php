		<p>Annett C. Richter, a native of Halle, Germany, is Lecturer of Musicology at North Dakota State University where she teaches courses in music history and music bibliography, as well as a four-semester sequence for doctoral students on writing a doctoral thesis. Prior to coming to NDSU, she worked at Minnesota State University Moorhead and at the University of Missouri, Columbia, where she taught courses in music history, music appreciation, American music, and writing about music. She also teaches courses in music history and music iconography at Concordia College in Moorhead, Minnesota.</p>

		<p>In her research, Dr. Richter has explored interdisciplinary topics of music iconography, intersections between music and painting, and, more broadly speaking, interrelationships among music, art, society, culture, and place in sixteenth- and nineteenth-century England and Scotland as well as in nineteenth- and twentieth-century America. She has delivered conference papers nationally and internationally at meetings of the American Musicological Society, the Society for American Music, the Renaissance Society of America, the College Music Society, the North American British Music Studies Association, the Missouri Folklore Society, the Hawaii International Conference on Arts and Humanities, as well as at conferences in England, Ireland, and Scotland. Her articles have appeared in <i>Musicological Explorations</i> and in the <i>Proceedings</i> of the 2004 Hawaii International Conference on Arts and Humanities. In her dissertation (2008), she investigated Missouri artist and folklorist Thomas Hart Benton (1889-1975) and his connection to music. Combining the analysis of Benton’s visual art, music transcriptions, sound recordings, and sources documenting his multi-faceted life as a musical collector and performer on harmonica, she shows that this artist straddled low-brow and high-brow musical cultures and ultimately created, in various locations throughout his career, his own unique musical practices in flux.</p>

		<p>As a musician, Richter has been active as lutenist and guitarist in Minneapolis/St. Paul and in Fargo-Moorhead. As lutenist, she has appeared in concert with sopranos Linh Kauffman, Candace Magner, Dawn Sonntag, and Kim Sueoka, lutenists Richard Griffith, Philip Rukavina and Tom Walker, and with Consortium Carrissimi. As a guitarist, she has presented solo and duo recitals for the Minnesota Guitar Society Local Artist Series in the Twin Cities. As a member of the Minnesota Guitar Society’s Board of Directors, she has conducted interviews for <i>The Guitarist</i> with internationally renowned artists, including guitarists Antigoni Goni and Berta Rojas, and lutenist Lucas Harris. Richter has judged auditions for the Minnesota Guitar Society Youth Guitarathon as well as for the Schubert Club and the Minnesota Music Teachers Association Classical Guitar Annual Scholarship Competitions. She also served as juror for the 2010 St. Joseph (Missouri) International Guitar Festival & Competition, founded and directed by guitarist Anthony Glise.</p>

		<p>Dr. Richter holds Master’s degrees in musicology and in guitar performance from the University of Minnesota and the equivalents of a B.A. and M.A. in British and American Studies from Martin-Luther-Universität Halle-Wittenberg, Germany. She received her Ph.D. in musicology from the University of Minnesota, Twin Cities, where she was awarded the Graduate School Doctoral Dissertation Fellowship and a Graduate School Dissertation Research Grant, as well as a Research Assistance Grant from the Sinfonia Educational Foundation. As a guitarist, she studied with Petra Burmann, Ursula Klein, and Peter Gollnast in Germany, and with Jeffrey Van and Todd Green in the United States. Dr. Richter has a long-standing interest in traditional Irish music and has played flute in Irish music sessions in Minnesota, Missouri, Montana, North Dakota, and Ireland.</p>
		
