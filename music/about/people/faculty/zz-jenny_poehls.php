        <p>Jenny Poehls currently serves as Lecturer of Flute at the Challey School of Music at North Dakota State University. Her duties include applied flute lessons and pedagogy and flute chamber ensembles. She holds a Master of Music degree from the University of Northern Iowa in flute performance and a Bachelor of Music degree in music performance and music education from North Dakota State University.</p>
        
        <p>A native of Harwood, ND, Ms. Poehls has been an active musician in the Fargo-Moorhead community since the age of fifteen, performing a wide variety of music for both flute and piano. Ms. Poehls graduated <i>summa cum laude</i> from North Dakota State University, and in 2006, she began her graduate studies at the University of Northern Iowa, studying flute performance and pedagogy with the highly esteemed Angeleita Floyd. Her teachers also include Sharon Boschee, Debora Harris, and Elizabeth Chaussé.</p>
        
        <p>A respected performer and educator, Ms. Poehls has been teaching in the F-M area since 2001.  She previously held a private piano and flute studio for 15 years and also worked with the Children’s Music Academy, a program designed to teach ear training, sight singing, theory, and keyboard skills to children ages four through eight.  In addition to her duties at North Dakota State University, Ms. Poehls holds a K-12 music education license from the state of North Dakota and teaches at South Elementary School in West Fargo. </p>
        
        <p>Ms. Poehls has performed with the Fargo-Moorhead Symphony Orchestra, Fargo-Moorhead Opera, NDSU Opera, and in master classes for such notables as Leone Buyse, the Imani Winds, Adam Kuenzel, and Emily Butterfield.  She has been principle flutist with the Fargo-Moorhead Opera, Northern Iowa Symphony Orchestra, Northern Iowa Wind Ensemble, and NDSU Wind Symphony.  She has been a featured flutist at the National Flute Association Convention, College Music Society Great Lakes Chapter Conference, NDSU Baroque Festival, with NDSU and MSUM music theatre, and the UNI Flute Choir. Ms. Poehls is presently performing in NDSU faculty chamber ensembles, including the Faculty Woodwind Quintet. </p>
		
		<p>UNI awarded Ms. Poehls a full tuition waiver and full assistantship to teach private flute, group piano, and work as a collaborative pianist for the opera and theatre programs. In the spring of 2008, she was invited to present her thesis, entitled <i>Imagery and Emotion in Music</i>, at the Graduate Research Symposium at UNI. She is a member of the National Flute Association, Upper Midwest Flute Association, and Phi Kappa Phi Honor Society.</p>
