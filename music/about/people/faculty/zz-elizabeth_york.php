        <p>Dr. Elizabeth York is the lecturer of upper strings at North Dakota State University. She also maintains a successful private studio of violin and viola students in the Twin Cities. Since 2013 she has taught at Birch Creek Music Performance Center in Door County, Wisconsin.</p>
		
        <p> Dr. York enjoys a busy and varied performance career that has found her performing with symphony orchestras, early music groups, chamber music ensembles, as an orchestral soloist, musical theater pit musician, and even for occasional pop, jazz, and free improvisation performances. She is a member and board member of Lyra Baroque Orchestra and regularly performs with the South Dakota Symphony Orchestra. She has served as guest concertmaster of Kenwood and Rochester Symphony Orchestras, and is the former concertmaster of Minnehaha Music Repertory Orchestra. Dr. York has performed as a soloist with the Raleigh Symphony and Minnehaha Music Repertory Orchestras, as a baroque violinist for St. Mark’s Episcopal Early Music series in Islip, New York, and in contemporary music performances at New York City’s Merkin Hall and Symphony Space.</p>
		
		<p>Recent chamber music performances have included appearances at Steinway Hall in New York City, Southampton Cultural Center, the Schubert Club’s courtroom concert series at the Landmark Center in St. Paul, and NDSU Chamber Music Festival. Dr. York has performed as a featured guest for the Four Seasons Chamber Music Festival “Next Generation” concert series in Greenville, North Carolina, and with members of the Emerson String Quartet as part of the Stony Brook Chamber Music Festival.</p>
        
		<p>Dr. York holds a B.M. from East Carolina University, and a M.M. from Stony Brook University, both degrees in violin performance, as well as a dual D.M.A. in violin and viola performance from Stony Brook University. Her major teachers include violinists Ara Gregorian, Soovin Kim, and Phil Setzer, and violists Dan Panner, Nicholas Cords, and Larry Dutton.</p>
