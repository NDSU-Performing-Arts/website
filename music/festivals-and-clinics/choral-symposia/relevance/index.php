						<h2>Featured Choir: Conspirare</h2>
					
					<p>We are pleased to announce Conspirare as our featured choir.</p>
		
					<p>Established in Austin, Texas in 1991, Conspirare is a Grammy-winning and internationally recognized choir with a reputation for “expanding the boundaries of choral performance” (Wall Street Journal). Conspirare, which translates from Latin as “to breathe together,” is led by founder and Artistic Director Craig Hella Johnson, and is comprised of soloists from around the country. Conspirare’s extensive discography includes 12releases on the harmonia mundi label. The 2014 album The Sacred Spirit of Russia won the Grammy for Best Choral Performance. Conspirare’s ambitious mission is to engage the power of music to change lives. Through its artistic excellence, creative programming, commissioning, and educational endeavors, the organization has established itself as an agent of change and a bedrock of the Texas arts community and beyond.</p>

					<p>Conspirare's most recent recording and touring project, Considering Matthew Shepard, is a three-part oratorio composed by Craig Hella Johnson. The work, which debuted at number four on Billboard’s Traditional Classical Chart, is an evocative and compassionate musical response to the murder of Matthew Shepard. The Washington Postcalls the impact “immediate, profound and, at times, overwhelming.” Conspirare debuted Considering Matthew Shepard in Austin in 2016, and presents the work at Boston Symphony Hall and Texas A&amp;M in 2016-17. A national tour is now in the planning stages for the 2017-18 season in observance of the 20th anniversary of Matthew Shepard’s death.</p>

					<p>For more information on Conspirare, please visit their website: <a href="https://conspirare.org">https://conspirare.org</a></p>
