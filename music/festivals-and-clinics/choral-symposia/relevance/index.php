					<h2>Tim Sharp</h2>
					
					<p>Tim Sharp (BM, MCM, DMA) is Executive Director of the American Choral Directors Association (ACDA), the national professional association for choral conductors, educators, scholars, students, and choral music industry representatives in the United States. He represents choral activity in the United States to the International Federation for Choral Music (IFCM) and serves as Vice-President.</p> 
		
					<p>Sharp, himself an active choral conductor, researcher, and writer, has varied his career with executive positions in higher education, recording, and publishing. Prior to his leadership of ACDA, Sharp was Dean of Fine Arts at Rhodes College, Memphis, TN, and earlier, Director of Choral Activities at Belmont University, Nashville, TN. </p> 
		
					<p>Tim’s research and writing focuses pedagogically in conducting and score analysis, and various published essays and books betray his eclectic interests in regional music history, acoustics, creativity, innovation, and aesthetics. He has conducted university, community, church, and children’s choirs, and continues to serve as choral conductor and clinician in the United States and internationally. He is in his tenth year as Artistic Director/ Conductor of the Tulsa Oratorio Chorus, Tulsa, OK.</p>
		
