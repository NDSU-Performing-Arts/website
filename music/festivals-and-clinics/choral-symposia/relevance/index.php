					<h2><em>Considering Matthew Shepard</em></h2>
					
					<p>In October of 1998, Matthew Shepard, a young, gay student at the University of Wyoming in Laramie was kidnapped, severely beaten, tied to a fence and left to die in a lonely field under a blanket of stars. Five days later, when Matt passed away, the world was watching. Approaching the eve of the 20th anniversary, Craig Hella Johnson has responded with his first concert-length work, <em>Considering Matthew Shepard</em>.</p>
		
					<p>Led from the piano by Johnson, <em>Considering Matthew Shepard</em> showcases the award-winning artistry of Conspirare’s singers with a chamber ensemble of renowned instrumentalists. This three-part fusion oratorio speaks with a fresh and bold voice, incorporating a variety of musical styles seamlessly woven into a unified whole. Johnson sets a wide range of poetic and soulful texts by poets including Hildegard of Bingen, Lesléa Newman, Michael Dennis Browne, and Rumi. Passages from Matt’s personal journal, interviews and writings from his parents Judy and Dennis Shepard, newspaper reports and additional texts by Johnson and Browne are poignantly appointed throughout the work.</p>
