					<h2>Rhonda Fuelberth</h2>
					
					<p>Rhonda J. Fuelberth is associate professor of choral music education at the University of Nebraska-Lincoln, where she serves as the Chair of Graduate Music Education. Dr. Fuelberth taught choral and general music in the Omaha, NE area and has worked with singers of all ages, from children through adults. She currently teaches undergraduate and graduate courses with a focus on choral music education and inclusive music education. She also co-conducts the University Chorale, an ensemble that has performed under her direction for numerous state, regional, and national conferences, and frequently serves as a clinician and guest conductor.</p>
				
				<p>Rhonda is the founder and artistic director for i2Choir**--Inclusive, Intergenerational, Exponentially Better Together, a community choral organization that seeks to provide a model for inclusive choral music experiences. She currently serves as the President-Elect of the North Central division of the American Choral Directors Association and is Past-President for the Nebraska Choral Directors Association. Her research has focused on choral music education, music teacher training, inclusive music education practices, and Universal Design for Learning as a model framework for inclusive music education. Rhonda has presented and published research at the state, regional, national, and international levels of the National Association for Music Education, the International Society for Music Education and the American Music Therapy Association.</p>
				
				<p>**i2Choir-- i2Choir is an inclusive and intergenerational choral ensemble that has partnered with the University of Nebraska-Lincoln Glenn Korff School of Music (GKSOM) and International Quilt Study Center and Museum (IQSCM) to provide music-making opportunities for individuals of all abilities. In i2Choir, individual contributions are valued, musical growth is championed, and all members are partners in creating high quality music. i2Choir members participate in cross-age, cross-ability groups or “singing teams” of two to four who want to sing in the ensemble together. Singing teams consist of singers who would like to sing with, and support family members and friends with a variety of physical, sensory, and cognitive challenges.</p></p>
	  
