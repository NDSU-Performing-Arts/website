						<h2>Tesfa Wondemagegnehu</h2>
					
					<p>Tesfa Wondemagegnehu serves as the conductor of the Viking Chorus and the Chapel Choir at St. Olaf College. He maintains an active voice studio and teaches Music and Social Justice with the goal of helping bridge the gap of social injustice through music, dialogue, and inspiring it’s participants to take action in their communities. Wondemagegnehu most recently was the Director of Choral Ministries at Westminster Presbyterian Church of Minneapolis where he co-founded the Justice Choir movement with noted composer Abbie Betinis. In the summer of 2018, Tesfa will serve as guest conductor of the children’s choir at the Aspen Music Festival, leading them in a week-long journey on the Justice Choir Songbook.</p>
		
					<p>Tesfa has conducted, given masterclasses, workshops, and clinics both nationally and internationally. In 2017 he co-led the American Choral Directors Association’s national high school honor choir working collaboratively with Eric Whitacre. In June of 2017, he was named the composer-in-residence for Choirs of America at Carnegie Hall, where he had the opportunity to conduct and lift over 600 voices in singing the world premiere of his composition May the Road. Wondemagegnehu made his Severance Hall debut in 2017, co-conducting with Cleveland Orchestra Chorus Master, Dr. Lisa Wong, a choir comprised of high school students from the Cleveland area, and in 2016 he worked with Los Angeles Master Chorale artistic director Grant Gershon, co-conducting their 1,000 student honor choir in the majestic Walt Disney Concert Hall.</p>

					<p>Tesfa holds a Master of Music degree in Choral Conducting and Vocal Performance from the Florida State University and a Bachelor of Music degree in Vocal Performing from the University of Memphis. </p>
					
