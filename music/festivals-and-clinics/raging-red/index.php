<h2>NDSU Wind Symphony in Performance</h2>
<p>The NDSU Wind Symphony will perform on both days of the festival. This is a wonderful opportunity for your students to hear a college-level band, composed of students much like themselves! Plan to build this performance into your schedule as part of your day at NDSU.</p>
		
<h2>Festival Format</h2>
        <p>Bands will perform for an adjudicated rating or for comments only.</p>
        <p>Each band will be given 25 minutes in the warm up area. Each band will proceed to the stage and will have 25 minutes to enter, perform, and exit the stage. Bands will then move to the clinic room where they will have 25 minutes with the adjudicator.</p>
        <ul>
          <li><strong>Classifications</strong>
            <ul>
              <li>Bands will be classified by grade level. Bands using multiple grades will register for the highest grade level in the band. Judges will be informed as to the grade level make up of each band.</li>
            </ul>
          </li>
          <li><strong>Repertoire</strong>        
            <ul>
              <li>Bands are encouraged to play 2 or more selections of contrasting styles. Bands will provide 3 original and numbered scores for the adjudication panel. Photocopies of scores are acceptable if accompanied by publisher&rsquo;s letter for those out of print or proof of purchase for backordered scores.</li>
            </ul>
          </li>
          <li><strong>Awards</strong><ul>
          <li>All bands have the option to participate for ratings or comments, only.</li>
          <li>Awards will be presented based on the average of the 3 adjudicator&rsquo;s scores
            <ul>
              <li>Gold Award- 90-100.                  Silver Award- 80-89                    Bronze Award-  79 and below</li>
              <li>Outstanding Section Awards will be given based on an adjudicator&rsquo;s recommendations.</li>
              <li>Outstanding Soloist Awards will be given based on an adjudicator&rsquo;s recommendations.</li>
            </ul></li>
            </ul>
          </li>
        </ul>
