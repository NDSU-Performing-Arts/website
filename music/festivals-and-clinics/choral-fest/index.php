		<p>We are pleased to invite you to the 34th Annual NDSU High School Choral Festival on <b>Sunday, October 6 </b>and <b>Monday, October 7, 2024</b>.  We look forward to this opportunity to work with your students and host you on our campus.</p>
 
		<p>The festival’s <b>registration will begin at 1:15 p.m.</b> on Sunday, October 1 and will be followed by a concert featuring the NDSU Concert Choir and Madrigal Singers at 2 p.m.  Joining us for the concert will be the West Fargo High School Madrigal Singers conducted by Brady Ritland. Rehearsals will begin after the concert. The <strong>Choral Festival Concert is scheduled for Monday, October 7 at 4:30 p.m.</strong> Admission to both concerts is free.</p>
 
		<p>We are excited to offer the following choirs for your students. The Tenor/Bass choir for 9th and 10th grade students will be conducted by Michael Weber; the Soprano/Alto choir for 9th and 10th grade students will be conducted by Charlette Moe. The auditioned Chamber Honor Choir for students in grades 11 &amp; 12 will be conducted by Dwight Jilek. The mixed choir for 11th and 12th grade students will be conducted by Dr. Ryan Smit. Ryan is the Director of Choral Activities at Dordt University in Sioux Center, IA. Ryan works with college student from around the region and has an excellent choral program.</p> 
 
		<p>Auditions for the NDSU High School Chamber Choir will be submitted via email to <a href="mailto:Dwight.jilek@ndsu.edu">Dwight.jilek@ndsu.edu</a> with a <strong>due date of September 11, 2024.</strong> Each student will submit an electronic file with the student singing a folksong or an art song – no Broadway or pop music. Students should identify which part they are auditioning for, i.e. soprano or alto. The size of the NDSU High School Chamber Honor Choir will be 24 – 32 voices. We will announce the selected singers on September 13, 2024.</p>  

		<p><strong>All students will be expected to have their music learned prior to the festival</strong> so they can create a true musical experience and enjoy creating music with their peers. It will be the student’s responsibility to get the music and learn it before the first rehearsal. Links for rehearsal tracks will be sent next week.</p>

		<p>For the other festival choirs, each school may send <strong>one SATB quartet</strong> to the Mixed Choir and a <strong>balanced quartet</strong> to the Soprano/Alto Choir and a <strong>balanced trio</strong> to the Tenor/Bass Choir. If you have any questions about participation, please do not hesitate to contact any of us. We will take registrations on a first-come basis. <strong>The registration fee for each student is $50</strong>, which includes a t-shirt, supper on Sunday night, and lunch on Monday. Choral directors will be the guests of the NDSU Challey School of Music.</p>
		
		
