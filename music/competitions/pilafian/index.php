<h2>Description of Final Award</h2>
<ul>
<li>The winner of the competition will compose a work for solo tuba with keyboard accompaniment in consultation with North Dakota State University (may be piano or other type of single-performer accompaniment).</li>
<li>The composition may be single- or multi-movement.  The composition should be at least 6 minutes in length; maximum length should be no more than 15 minutes.</li>
<li>The composer will be present at the premiere of the work, which will be performed on the campus of North Dakota State University (support will be provided towards travel and expenses by the university).</li>
<li>Performance of the work will be by a regionally/nationally prominent artist of tuba to be announced.</li>
