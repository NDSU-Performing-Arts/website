<h3>Competition Information</h3>
<p>North Dakota State University is pleased to announce the formation of a composition contest in memory of the late Sam Pilafian. The winning composer will receive a commission to compose a new work as a memorial to the work of Sam Pilafian in his time as a guest faculty member at NDSU as well as his contributions to the music world.<p>

<h2>Description</h2>
<p>J. Samuel Pilafian had a remarkable career in music through a variety of areas, including his role as a founding member of the Empire Brass Quintet and later a member of the Boston Brass; as an internationally renowned tuba and brass pedagogue at several universities; and as a composer and arranger for a large library of solo and ensemble works. Along with the Boston Brass in his final days, he forged a relationship with North Dakota State University in developing a new approach to teaching music in the universities that would include all areas of preparation in music towards creating a stronger music student. His untimely death in 2019 robbed the music world of a truly remarkable, diverse musical talent.</p>

<p>As part of its commemoration of the memory of Sam Pilafian, North Dakota State University is establishing a composition contest that focuses on Sam’s first love: the tuba. The competition is structured to develop an original composition in his memory that promotes new, original music for the tuba as well as potentially providing composers a forum for increasing awareness of their work.</p>
</div>
	
