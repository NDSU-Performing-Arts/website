		<h3>Guest Artists</h3>
		
		<p>CARLOS ABRIL is a professor of music and director of undergraduate music education at the Frost School of Music at the University of Miami, where he teaches courses in music in childhood, philosophy of music and cultural diversity in music education. He has published numerous articles and chapters in research journals, professional magazines and books. He co-edited books on music education through Oxford University Press and Rowman &amp; Littlefield, and serves on editorial boards in North America, South America and Europe. His music arrangements and instructional materials are published by World Music Press and in the Macmillan/McGraw-Hill “Spotlight on Music” textbook series.</p>
		
		<p>SAM PILAFIAN is a virtuoso on tuba, the tubist/arranger of the Boston Brass and a distinguished visiting professor at NDSU’s Challey School of Music. He has recorded and performed with the Boston Symphony Orchestra, the New York Philharmonic Orchestra, the Orchestra of St. Luke’s, the Metropolitan Opera Orchestra, the Duke Ellington Orchestra, Lionel Hampton and Pink Floyd, among others. He has earned prestigious honors such as an Emmy for Excellence in Instructional Video Production, the Walt Disney Award for Imagination and Innovation in Design, the Walter Naumberg Chamber Music Award and the Harvard Music Association Prize. He is the co-author of the best-selling pedagogy texts and DVDs “Breathing Gym” and “Brass Gym.” He also has served on the faculties of Boston University, the Tanglewood Institute, Berklee College of Music, Frost School of Music and Arizona State University.</p>
		
		<p>ANDREA STRAUSS is the conductor and artistic director of the Tara Winds Community Band and has taught elementary, middle, high school and college students at both public and private schools in Georgia for 30 years. She is the former director of bands at Georgia Tech and associate professor of music at Shorter University. She has conducted in Japan and Italy, adjudicated in Canada and Ireland and conducted all-state and honor bands across the United States. She has presented at clinics for the National Association for Music Education (NAfME) National and Division In-Service Conferences, Texas Bandmasters Association (TBA) Convention, numerous GMEA In-Service Conferences and various universities. Her articles have been published in the Music Educators Journal and the National Band Association Journal.</p>
