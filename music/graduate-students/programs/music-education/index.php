	<h1>FAQs</h1>
	<p><strong>What is the timeline for degree completion?</strong><br />
	Complete the degree in five semesters (two in the summer) or within seven years.</p>

	<p><strong>What is the tuition?</strong><br />
	The tuition is <a href="https://www.ndsu.edu/onestop/tuition-fee-charts">low and affordable</a>.</p>

	<p><strong>What courses are available online?</strong><br />
	All of our courses are available online and MUSC 702, 704, and 731 are offered online every semester.</p>

	<p><strong>Are the online courses offered asynchronously or synchronously?</strong><br />
	The online courses are designed for flexibility and offered asynchronously to accommodate teacher’s schedules. A few synchronous meetings are required for MUSC 701 and 703. MUSC 790 is the only online course which requires synchronous attendance.</p>

	<p><strong>What is MUSC 790 – The Seminar in Contemporary Issues in Music Education?</strong><br />
	MUSC 790 is the only course which requires synchronous attendance, but we are actually encouraging students to campus if they are available. This Seminar (Music Education Symposium) is a fantastic ONE WEEK course which completes three credits. During that week, we have headliners such as Bridget Sweet, Birch Browning, Mary Goetze, Peter Boonshaft, Robert Duke, Carl Hancock, Kenneth Elpus, Andrea Strauss, Carlos Abril, Alice-Ann Darrow, Tim Sharp and Deborah Sheldon join us on campus to share their areas of expertise.</p>
	
	<p><strong>Will I connect with my colleagues and professors during online classes?</strong><br />
	Even though the degree is online, through Discussion Boards in our elective course offerings and MUSC 790, teachers pursuing this degree are able to connect with other colleagues and faculty, while gaining relevant knowledge<br /><br />
	MUSC 790 is the only online course which requires synchronous attendance, and we also invite our teachers that are pursuing the degree to our beautiful campus. Summers are gorgeous in Fargo, North Dakota.</p>

	<p><strong>What is MUSC 731?</strong><br />
	MUSC 731 is Applied Lessons, these lessons are typically in the teacher’s area of expertise but sometimes in an area in which they hope to gain knowledge. Applied Lessons are flexible and scheduled between the student and instructor and can be offered online or in person, if living in close proximity to North Dakota State University in Fargo, North Dakota.</p>

	<p><strong>What are the required registration details when completing the degree?</strong><br />
	Teachers pursuing the MMME only need to complete six-credits per calendar year (January-December) to remain in the program.</p>

	<p><strong>What is MUSC 794 and the requirements for comprehensives?</strong><br />
	Creating your practicum for the four-credits of MUSC 794 begins with your thoughts about your current teaching situation. What are areas in your teaching that you wish to strengthen or further organize? What is a content area that you wish you were teaching or incorporating but need an extra motivation to create it? Our practicums have ranged from standards, to assessments, to seating arrangements, to diversity, to units to develop compound counting systems, to adaptive music, to “guitar hero” structured for orchestra. The practicum is a self-guided project, guided by your advisor. The practicum is viewed as a "work in progress," as we know that the art of teaching is constantly evolving. Comprehensives are evidence that you have gained knowledge that aligns with our NASM accreditation. Comprehensives also offer an additional opportunity to communicate the results of your practicum and  implementations that you have integrated into your teaching through the MMME coursework.</p>

	<p><strong>Will I receive good advising?</strong><br />
	At NDSU, we pride ourselves in our personal approach to assisting teachers in completing this degree. We thrive on building connections with currently practicing teachers.</p>

	<p><strong>What is a typical plan and schedule for completing the courses?</strong><br />
	Again, the degree is flexible and course enrollment can start in the fall, spring or summer semesters. The only enrollment requirement is to complete six credits per calendar year (January-December) and to complete the degree in seven years. If you were hoping to complete the degree in five semesters, here is a suggested plan….<br />
	Summer 1: MUSC 703 and MUSC 790<br />
	Fall 1: MUSC 702 and MUSC 715 or 765<br />
	Spring 1: MUSC 704 and MUSC 713 or 766<br />
	Summer 2: MUSC 701, MUSC 731 and 2 credits of MUSC 794<br />
	Fall 2: MUSC 758 and 2 credits of MUSC 794</p>
	
