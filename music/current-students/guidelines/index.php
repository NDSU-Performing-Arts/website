			<li>MUSC 480 - Preparation and presentation of a full recital, typically presented in a student's senior year for performance majors.</li>
			<li>MUSC 780 - Preparation and presentation of a professional, full length recital.</li>
		</ul>
		
		<p><strong>Step One</strong></p>
		
		<ul>
			<li>Register for MUSC 380, 480, or 780 within the first two weeks of the semester. 780 is available for variable credit: Check with your advisor for the correct number of credits to take.</li>
		</ul>
			
		<p><strong>Step Two: 4 (or more) months prior</strong> </p>
			
		<ul>
			<li> Select a recital date and dress rehearsal date. Forms and schedule for Beckwith Recital Hall are available on our <a href="http://bit.ly/PerfArtsSharepoint">Sharepoint Page</a>. Students must submit all necessary forms for both the dress rehearsal and the recital date. Rehearsals should be scheduled on Monday, Tuesday, or Wednesday at 5:00, 6:30, or 8:00 p.m. Recitals should be scheduled for Thursday or Friday at 5:30 or 7:30 p.m.; Saturday or Sunday at 2:00, 4:00, or 6:00 p.m. Dates must be approved by instructor and all collaborating musicians.</li>
			
			<li>After choosing a date, complete and submit a <a href="https://forms.gle/HTSA3yA7Tn1WACKm9"><strong>Reservation Request Form</strong></a>. The date of the recital performance and dress rehearsal should be in the same week. Any additional staffing or technical needs should also be submitted at this time (i.e. need of microphones, projection, etc.)</li>
			
			<li><strong>The submission of a Reservation Request Form does not guarantee the booking of the requested date and time. An email confirmation will be sent once the Reservation Request Form has been approved and processed. Reservation Requests will be processed on a first come, first served basis.</strong></li>
		</ul>
		
		<p><strong>Step Three: 1 (or more) months prior</strong></p>
		<ul>
		<li>Schedule the recital hearing (more information below).</li> 
		<li>Prepare your recital program. Give yourself ample time to research, develop, and create your program using our <strong><a href="../documents/ConcertTemplate.docx">Program Template</a></strong>.</li>
		<li>Submit the first draft of your program, along with any notes or translations (if required) to the applied teacher. We have a <strong><a href="https://www.ndsu.edu/performingarts/music/current-students/documents/recitals/notes_translations_template.docx">Notes/Translations Template</a></strong> you may use, if applicable.</li>
		</ul>
		
		<p><strong>The Hearing: </strong></p>
		<ul>
		<li>The hearing must occur at least two weeks before the recital (one month for vocal).</li>
		<li>The final version of the program and translations/program notes (approved by applied teacher) must be submitted at or before the hearing.</li>
		<li>The collaborative pianist (and other collaborators unless excused by the applied instructor) must be present and perform at the hearing.</li>
		<li>Failure to abide by the above will automatically result in postponing or cancelling the recital.</li>
		<li>Insufficient preparation for the hearing will result in postponing or cancelling the recital at the discretion of the hearing committee.</li>
		</ul>
		
		<p><strong>Step Four: 2 weeks prior</strong></p>
			
		<ul>
			<li>Immediately after successful recital hearing, the next step is to email your program file and any notes or translations to our Promotions Director.</li>
			<li>The Challey School of Music will upload the digital program and accompanying materials to our <a href="https://drive.google.com/drive/folders/1NAuhtRqLnOhI9KXtpz70T8EdG3DMkZmD">Google drive</a>. <strong>Students are responsible for printing their own programs and notes/translations.</strong></li>
			<li>Submit stage diagram(s) and discuss dress rehearsal/recital logistics with our Concert Facilities Manager. <strong>Please note:</strong> For student recitals, there is no staffing for rehearsals or backstage for performances. Students are tasked with finding their own backstage crew for performance. Students who do not recruit backstage help may experience complications involving backstage shifts and start of show coordination.</li>
		
