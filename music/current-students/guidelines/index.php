		<p>Preparing for your student recital can be a complex, yet exciting task for music majors. The information below should guide you towards a successful recital. <strong>Remember, it is up to you to make sure this process moves smoothly</strong>. Do not count on your studio instructor or office staff to make sure you complete the necessary steps in presenting a recital.</p>
