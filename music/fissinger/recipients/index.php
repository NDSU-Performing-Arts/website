		<h2>2015 First Prize: Joshua Hummel</h2>
            	<p><strong>Title</strong>: <em>May Night</em><br />
              	<strong>Performance Date</strong>: October 24, 2015, 7:30 PM – Festival Concert Hall</p>
            	<p>Joshua Hummel received the 2015 Edwin Fissinger Choral Composition Prize winner for his work <em>May Night</em>, text by Sara Teasdale. 
				The work premiered at NDSU’s Contemporary Composition in America Choral Symposium during the October 24 concert with the NDSU Concert Choir.</p>
             	<p>Hummel is the recipient of various composition awards including the prestigious Frederick Fennell Prize and the Leonard Bernstein Award. His music has been performed in Carnegie Hall and throughout the United States, as well as Paris, Croatia, Perugia, Rome, London and Moscow. Hummel holds an M.A. in theatre and a M.Mus. in composition and has studied with professors from the Hartt School of Music, San Francisco Conservatory and Julliard.</p>
				
				<p>Hummel has received several awards for his choral music, including the 2014 Loyola University Maryland Prize for Composition with his setting of <em>God of Our Fathers</em>. His wind ensemble music has been performed throughout the country by some of the nation’s top college bands. In February 2011 the Yale Concert Band premiered his Fennell award-winning composition, <em>Haiku Symphony No. 4</em>, in Woolsey Hall and Carnegie Hall. Hummel composes for Colors in Motion, an arts collective in Cambridge, Massachusetts, and owns Sinensis Music, a composition studio in Hartford, Connecticut.</p>
