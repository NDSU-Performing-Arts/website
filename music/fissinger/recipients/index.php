		<h2>2018 First Prize: Stephen Shewan</h2>
		
		<p><strong>Title</strong>: <em>Mother Goose Gems</em><br />
			<strong>Performance Date</strong>: March 17, 2019, 2:00 PM – Peace Lutheran Church</p>
		
		<p>NDSU Challey School of Music is pleased to announce the winner of the 2018 Edwin Fissinger Choral Composition Prize: "Mother Goose Gems" by Stephen Shewan. Stephen Shewan has composed music for numerous media, including orchestra, string quartet, chamber ensembles, symphonic band, solo voice, choir and piano. His music has been performed by the Rochester Philharmonic Orchestra, the Eastman Wind Ensemble, the US Army Strings, The Master Chorale of Tampa Bay, the Vrije Univeriteit Amsterdam Choir, and numerous other choirs, orchestras, bands, chamber ensembles and soloists across the United States, Europe and Australia. Shewan’s music can be heard on Music of Stephen Shewan, Albany Records (TROY149) The Road Less Traveled, Albany Records (TROY783) and Stephen Shewan Orchestral and Instrumental Music, Albany Records (TROY 1569). Shewan’s music has been broadcast on over 200 radio stations in America on NPR's Performance Today and on Exploring Music with Bill McGlaughlin.</p>

		<p>Because Shewan’s father, Robert, was the director of the Roberts Wesleyan College Chorale for nearly forty years, and his mother, Nancy, conducted children’s choirs, it is no surprise that he has a deep affinity for writing choral music. Shewan’s “Silent Night,” for SATB choir and piano, was the winner of the 2016 Master Chorale of Tampa Bay Christmas Carol contest. The premiere performance can be heard at on youtube here. It has since received numerous performances and is now available in a version for choir and orchestra as well (Jubal Press). Shewan received the 2011 Robert H. Campbell Endowed Choral Composition Prize at the annual Choral Composition Festival at the Ithaca College School of Music for his “Musical Zoo.” Composer John Rutter wrote, “Stephen Shewan is a considerable composer who deserves a wide audience.”</p>

		<p>Shewan is the co-founder of the Williamsville Poetry and Music Celebration, which received the 2011 Award for Excellence and Innovation in the Arts from the College Board. He is a founding member of the New York State School Music Association (NYSSMA) Composition Committee and the Eastern Division Representative to the NAfME Council for Music Education in composition. Shewan is a member of the American Composers Forum and ASCAP. He played horn in the Cayuga Chamber Orchestra from 1985-1992. In 2000, he received the Buffalo Philharmonic Orchestra/Erie County Council of Music Coordinators Award for Excellence in Music.</p>

		<p>Since 1993, Stephen Shewan has been a music teacher at Williamsville East High School near Buffalo, New York. He taught music at Odessa-Montour High School from 1983-1992. He received a BS in Music Education from Roberts Wesleyan College, an MM in Horn Performance from Ithaca College, and a DMA in Music Education/Composition from the Eastman School of Music where his primary teacher was Samuel Adler. He is an active guest composer, conductor, pianist, and clinician. Shewan lives with his beautiful wife, Ruthie, his dog Jubal, and his cat Precious.</p>
		
