	<h2>The Undergraduate Degrees</h2>
	
	<p class="alert alert-success">At NDSU, we offer the Bachelor of Music in Composition. Here's what that means.</p>
	
	<p>In the United States, Bachelor's degrees usually denote a typical 4-5 year college curriculum. Bachelor's degrees in music usually take the form of the "Academic" degrees (the Bachelor of Arts (BA) or Bachelor of Science (BS) degrees) and the "Professional" degrees (the Bachelor of Music, the Bachelor of Music Education, the Bachelor of Fine arts, and so on).</p>
	
	<p><em>This isn't to say that the BM isn't Academic in nature or that the BA isn't Professional—these are just the categories we call them.</em></p>
	
	<p>The BA and BS degrees are usually focused towards a more general plan of study: 30%-45% of your classes will be in music, while the rest will be general education and general knowledge. Many schools recommend or require that students take classes towards another area of specialization (a major or minor) or take a foreign language. These are sometimes called "general" music degrees, because they often don't have a specialization, such as a BA in Music rather than a BA in Composition.</p>
	
	<p class="alert alert-success">At NDSU, the BS in Music requires a second major or a minor, while the BA in Music requires two years of a foreign language.</p>
	
	<p>The "Professional" degrees are made up of at least 50%-65% music classes, and usually have a specialization such as performance, composition, or education. Study is less concerned with breadth of education (although students in both the Academic and Professional degrees will take classes in music theory, music history, ensembles, and applied lessons).</p>
	
	<p><em>So which is better?</em> Neither, really—they just fulfill different purposes. Music is largely a competency-based field: If you can compose music, then you're a composer. (Music Education, on the other hand, you almost always have to have a degree in that to teach.) The two degree types are more about the philosophy of education rather than the content.</p>
	
	<p>At NDSU, you can take composition lessons as a BM Composition major, or as a BM Performance or Education major, or as a BA/BS in general Music major.</p>
	
	<h2>The Graduate Degrees</h2>
	
	<p class="alert alert-success">At NDSU, we don't offer graduate degrees in music. What follows is a general explanation of graduate music degrees.</p>
	
	<p>These differences carry over to the graduate world as well. At the Master's level (typically two years of study after the undergraduate/bachelor's level) there are the Master of Arts (MA) and the Master of Music (MM). We don't often see the Master of Science in Music, unless it's in something like Music Education or Music Technology. They're rarer, but they exist.</p>
	
	<p>There are also specialized degrees like the Master of Music Education (MME). Church Music and Sacred Music often have their own specialized degrees. There's also the Master of Fine Arts (MFA), which is usually a three-year program. We see this a lot in Theatre, but sometimes we see them in music too.</p>
	
	<p>Now, <em>typically,</em> the MM is used for artistic disciplines in music, while the MA is used for academic disciplines in music. If you're interested in conducting, performance, or composition, you'll often see MM programs. If you're interested in musicology/music history, or music theory, that's usually offered as an MA program.</p>
	
	<p>Typically, but not always. There are MM programs in theory and history, and there are MA programs in performance and conducting. Sometimes this is historical, and sometimes it's philosophical. The MA is usually a more research-oriented degree (in that it requires a thesis), while the MM is more performance-related, and requires a recital.</p>
	
	<p>In composition, the MM is currently more prevalent. However, there are plenty of programs that consider Composition to be more of a research discipline rather than a performance discipline. Really, it's kind of both. So MA and MM programs in composition aren't uncommon.</p>
	
	<p>At the doctoral level, the fields that are MM fields typically result in the Doctor of Musical Arts (DMA), while the MA fields typically result in the Doctor of Philosophy (PhD). Conducting, and performance are almost always DMA programs, while musicology and theory are almost always PhD programs. (I don't know of any that aren't). Music education can sometimes be a DMA, and sometimes a Doctor of Education (EdD) but are most often PhD programs. A couple of schools offer the Doctor of Arts (DA) instead of the DMA for performance degrees.</p>
	
	<p>Composition shows up most commonly as DMA programs, but several schools do offer a PhD. And some schools offer both the DMA and PhD.</p>
	
	<p>Is one degree better than the other? No. Both the PhD and the DMA require approximately 2-3 years of coursework, comprehensive qualifying written and oral exams, a dissertation proposal, a written dissertation, and a defense of the dissertation.</p>
				

