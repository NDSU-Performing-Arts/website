		<p>The Challey School of Music prepares undergraduates for careers as music teachers and as performers through the two tracks of the Bachelor of Music degree (BMus). Music Education track graduates are certified to teach music in the public schools of North Dakota, at any level—elementary, middle school, and high school, in both instrumental and voice/choral programs. Certification to teach K-12 music in other states is generally easily accomplished administratively because of our broad-based program. Performance degree students are prepared for careers as professional performing musicians, either immediately or after further study in graduate school. Our liberal arts degrees (BA and BS) prepare students for a wide range of careers that require critical thinking skills, versatility, and trainability in many disciplines.</p>
     
        <h2><a name="studentorganizations" id="studentorganizations"></a>Student Organizations        </h2>
        
		<h3>Sigma Alpha Iota </h3>
        
		<p> Sigma Alpha Iota is North Dakota State University's only professional fraternity for women in music. SAI was founded in 1903 at the University 
         of Michigan at Ann Arbor and has grown to over three hundred chapters and 76,000 members.</p>
        
		<p>SAI's purpose is to encourage and promote interaction between those who share a special interest in music. The members hold monthly meetings, perform in special recitals called musicales, perform off-campus, and support the bands and choral groups on campus. </p>
        <p>Membership in SAI is open to all women pursuing a major or minor in any area of music who have completed at least one semester of study. Rush is held twice a year, at the beginning of each semester. </p>
        <p>Members work together and support each other in their various activities. The Fargo-Moorhead SAI Alumni chapter offers two scholarships each year. Members may also be nominated by the chapter for national scholarships. For many members, the greatest benefit of being an SAI is the relationships 
        developed with other women in music, those who will be future colleagues.</p>
        <h3>National Assocation for Music Education (NAfME)</h3>
        <p>The National Association for Music Education is one of the world’s largest arts education organizations. NAfME advocates for all aspects of music education at local, state, and national levels. Joining NAfME Collegiate provides students with an opportunity for professional development at the undergraduate level which includes free or heavily discounted registration to state and national conferences, access to job search advice, periodicals and other scholarly articles, and free sheet music and lesson plan libraries. <br />
          <br />
        NAfME at NDSU provides students with many opportunities for professional development including monthly discussions hosting teachers from the area and specialists from around the country, as well as information sessions about Music Education at NDSU, partial to full funding from the Congress of Student Organizations to attend state and national music education conferences, and leadership opportunities which build practical skills in preparation for a career in music education.<br />
        </p>
